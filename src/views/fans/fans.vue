<template>
  <!-- 粉丝卡片区域 -->
  <el-card class="box-card">
    <!-- 命名插槽，头部内容 -->
    <div slot="header" class="clearfix">
      <span>粉丝统计</span>
    </div>
    <!-- 匿名插槽，内容主体 -->
    <div class="text item">
        <!-- 图表显示占位符 -->
        <div id="main" style="width:600px;height:400px"></div>
    </div>
  </el-card>
</template>

<script>
// 引入echarts
import echarts from 'echarts'
export default {
  name: 'Fans',
  mounted () {
    this.painPic()
  },
  methods: {
    painPic () {
      // 基于准备好的dom，初始化echarts实例
      var myChart = echarts.init(document.getElementById('main'))
      // 绘制图标
      myChart.setOption({
        title: {
          text: '某站点用户访问来源',
          subtext: '纯属虚构',
          x: 'center'
        },
        tooltip: {
          trigger: 'item',
          formatter: '{a} <br/>{b} : {c} ({d}%)'
        },
        legend: {
          orient: 'vertical',
          left: 'left',
          data: ['直接访问', '邮件营销', '联盟广告', '视频广告', '搜索引擎']
        },
        series: [
          {
            name: '访问来源',
            type: 'pie',
            radius: '55%',
            center: ['50%', '60%'],
            data: [
              { value: 335, name: '直接访问' },
              { value: 310, name: '邮件营销' },
              { value: 234, name: '联盟广告' },
              { value: 135, name: '视频广告' },
              { value: 1548, name: '搜索引擎' }
            ],
            itemStyle: {
              emphasis: {
                shadowBlur: 10,
                shadowOffsetX: 0,
                shadowColor: 'rgba(0, 0, 0, 0.5)'
              }
            }
          }
        ]
      })
    }
  }
}
</script>

